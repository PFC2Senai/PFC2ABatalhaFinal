public class DetServicoFuncionario {
    
    private int idDetServFuncionario;
    private int codFuncionario;
    private int codServico;

    /**
     * @return the idDetServFuncionario
     */
    public int getIdDetServFuncionario() {
        return idDetServFuncionario;
    }

    /**
     * @param idDetServFuncionario the idDetServFuncionario to set
     */
    public void setIdDetServFuncionario(int idDetServFuncionario) {
        this.idDetServFuncionario = idDetServFuncionario;
    }

    /**
     * @return the codFuncionario
     */
    public int getCodFuncionario() {
        return codFuncionario;
    }

    /**
     * @param codFuncionario the codFuncionario to set
     */
    public void setCodFuncionario(int codFuncionario) {
        this.codFuncionario = codFuncionario;
    }

    /**
     * @return the codServico
     */
    public int getCodServico() {
        return codServico;
    }

    /**
     * @param codServico the codServico to set
     */
    public void setCodServico(int codServico) {
        this.codServico = codServico;
    }
}
