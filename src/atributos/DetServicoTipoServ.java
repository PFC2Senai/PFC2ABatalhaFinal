public class DetServicoTipoServ {
    
    private int idDetServTipo;
    private int codTipo;
    private int codServico;

    /**
     * @return the idDetServTipo
     */
    public int getIdDetServTipo() {
        return idDetServTipo;
    }

    /**
     * @param idDetServTipo the idDetServTipo to set
     */
    public void setIdDetServTipo(int idDetServTipo) {
        this.idDetServTipo = idDetServTipo;
    }

    /**
     * @return the codTipo
     */
    public int getCodTipo() {
        return codTipo;
    }

    /**
     * @param codTipo the codTipo to set
     */
    public void setCodTipo(int codTipo) {
        this.codTipo = codTipo;
    }

    /**
     * @return the codServico
     */
    public int getCodServico() {
        return codServico;
    }

    /**
     * @param codServico the codServico to set
     */
    public void setCodServico(int codServico) {
        this.codServico = codServico;
    }
}
