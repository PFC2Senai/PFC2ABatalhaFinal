public class DetServicoProduto {
    
    private int idDetServProduto;
    private int codDetProduto;
    private int codServico;
    private int quantidade;

    /**
     * @return the idDetServProduto
     */
    public int getIdDetServProduto() {
        return idDetServProduto;
    }

    /**
     * @param idDetServProduto the idDetServProduto to set
     */
    public void setIdDetServProduto(int idDetServProduto) {
        this.idDetServProduto = idDetServProduto;
    }

    /**
     * @return the codDetProduto
     */
    public int getCodDetProduto() {
        return codDetProduto;
    }

    /**
     * @param codDetProduto the codDetProduto to set
     */
    public void setCodDetProduto(int codDetProduto) {
        this.codDetProduto = codDetProduto;
    }

    /**
     * @return the codServico
     */
    public int getCodServico() {
        return codServico;
    }

    /**
     * @param codServico the codServico to set
     */
    public void setCodServico(int codServico) {
        this.codServico = codServico;
    }

    /**
     * @return the quantidade
     */
    public int getQuantidade() {
        return quantidade;
    }

    /**
     * @param quantidade the quantidade to set
     */
    public void setQuantidade(int quantidade) {
        this.quantidade = quantidade;
    }

}
