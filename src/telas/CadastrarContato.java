    private void jBtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCadastrarActionPerformed
        
        PessoaContato pContato = new PessoaContato();
        Telefone tel = new Telefone();
        
        pContato.setCodCliente(1);
        pContato.setCodContato(1);
        pContato.setNomeContato(txtNovoContato.getText());
        tel.setTel(txtTel.getText());
        tel.setCel(txtTelCel.getText());
        PessoaContatoDAO.CadPessoaContato(pContato);
        ContatosDAO.CadTel(1, tel);
        ContatosDAO.CadEmail(1, txtEmail.getText());
        limparCampos();        
    }//GEN-LAST:event_jBtnCadastrarActionPerformed

    private void limparCampos(){
        txtEmail.setText("");
        txtNovoContato.setText("");
        txtTel.setText("");
        txtTelCel.setText("");
    }

