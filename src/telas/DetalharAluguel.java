    public void CarregaAluguel() {

        ArrayList<Aluguel> aluguel = new ArrayList<Aluguel>();
        aluguel = AluguelDAO.CarregaAluguel(idAluguel);

        for (Aluguel al : aluguel) {

            idCliente = al.getTabclienteIdcliente();
            txtDataLocacao.setText(String.valueOf(al.getDataAluguel()));
            txtDataDevolucao.setText(String.valueOf(al.getDataDevolucao()));
            txtTotal.setText(String.valueOf(total));
            txtDescricao.setText(al.getDescricaotLocacao());
            txtHora.setText(String.valueOf(al.getHora()));
        }

        ArrayList<Cliente> cliente = new ArrayList<Cliente>();
        cliente = ClienteDAO.CarregaNomeCliente(idCliente);

        for (Cliente cli : cliente) {
            txtCliente.setText(cli.getEmpresa());
        }
    }

    public void TabelaEquipamento(String Sql) {

        try {

            total = 0;
            stmt = getConnection().createStatement();
            ArrayList dados = new ArrayList();
            String[] Colunas = {"Código", "Equipamento", "Modelo", "Fabricante", "Qtd.", "Vr. Locação", "Total"};

            ResultSet rs;
            rs = stmt.executeQuery(Sql);

            while (rs.next()) {
                double totalEquipamento = rs.getInt("quantEquipamento") * rs.getDouble("valorLocacao");
                dados.add(new Object[]{rs.getObject("idDetLocacao"), rs.getObject("equipamento"),
                    rs.getObject("modelo"), rs.getObject("fabricante"),
                    rs.getObject("quantEquipamento"), rs.getObject("valorLocacao"),
                    totalEquipamento});
                total += totalEquipamento;
            }

            txtTotal.setText(String.valueOf(total));
            for (int i = 0; i < 7; i++) {

                ModeloTabela modelo = new ModeloTabela(dados, Colunas);
                jTableEquipamento.setModel(modelo);
                jTableEquipamento.getColumnModel().getColumn(i).setPreferredWidth(150);
                jTableEquipamento.getColumnModel().getColumn(i).setResizable(false);
                jTableEquipamento.getTableHeader().setReorderingAllowed(false);
                jTableEquipamento.setAutoResizeMode(jTableEquipamento.AUTO_RESIZE_OFF);
                jTableEquipamento.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            }

        } catch (SQLException ex) {
            Logger.getLogger(DetalharServico.class.getName()).log(Level.SEVERE, null, ex);

        } catch (Exception erro) {
            Logger.getLogger(DetalharServico.class.getName()).log(Level.SEVERE, null, erro);
        }
    }

    private void populaComboBoxCliente() {

        Connection conexao = Conexao.getConnection();
        ResultSet rs;
        String sql = "select * from tabcliente";

        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                jComboBoxCliente.addItem(rs.getString("empresa"));
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void idClienteComboBox() {

        Connection conexao = Conexao.getConnection();
        ResultSet rs;
        String sql = "select * from tabcliente where empresa = '" + jComboBoxCliente.getSelectedItem() + "';";

        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                codCliente = (rs.getInt("idcliente"));
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

