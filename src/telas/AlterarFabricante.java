    private void CarregaFabricante() {
        
        ArrayList<Fabricante> fabricante = new ArrayList<Fabricante>();
        fabricante = FabricanteDAO.CarregaFabricante(GetIndice());
        
        for (Fabricante fab : fabricante){
            jTextFabricante.setText(fab.getFabricante());
            id.setText(String.valueOf(fab.getIdFabricante()));
        }
    }

